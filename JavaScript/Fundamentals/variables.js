// Declarando variáveis:
// Var pemite reclarar a váriavel mais de uma vez:
var a = "Texto";
var b = 15; // Número

// Recomendação:
// Não permite redeclarar uma variável
let c = "Texto";
let d = 15;

// Contantes não permitem reclarar nem alterar valor atribuido anteriormente:
const pi = 3.14;
const nome = "Texto";

console.log(`variável a: ${a}`);
console.log(`variável b: ${b}`);
console.log(`variável c: ${c}`);
console.log(`variável d: ${d}`);
console.log(`variável b: ${pi}`);
console.log(`variável b: ${nome}`);
